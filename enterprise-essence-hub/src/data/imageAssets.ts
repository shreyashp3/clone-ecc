/**
 * Get image by key with fallback
 */
export const getImageUrl = (category: string, key: string, defaultKey = "default"): string => {
  const categoryMap = imageAssets[category as keyof typeof imageAssets] as Record<string, string> | undefined;
  
  if (!categoryMap) {
    return imageAssets.features.teamCollaboration; // Fallback to generic image
  }

  return categoryMap[key] || categoryMap[defaultKey] || imageAssets.features.teamCollaboration;
};

/**
 * Optimize image URL for different sizes
 */
export const optimizeImageUrl = (
  url: string,
  width: number,
  height: number,
  quality: "low" | "medium" | "high" = "high"
) => {
  if (!url) return "";
  
  const qualityMap = { low: 60, medium: 80, high: 90 };
  
  // For Unsplash URLs
  if (url.includes("unsplash.com")) {
    const [baseUrl] = url.split("?");
    return `${baseUrl}?w=${width}&h=${height}&fit=crop&q=${qualityMap[quality]}`;
  }

  // For Pexels URLs
  if (url.includes("pexels.com")) {
    return url; // Pexels URLs are already optimized
  }

  return url;
};
